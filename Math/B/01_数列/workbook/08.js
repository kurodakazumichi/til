//-----------------------------------------------------------------------------
// Σを式に書き下す問題
//-----------------------------------------------------------------------------
// Σで表された式を和の式に書きくだす。
// 第n項は5とする。
const N = 5;

//-----------------------------------------------------------------------------
// Σ{k=1}{n}(k^2)
//-----------------------------------------------------------------------------
function work01() {

  let exp1 = "";
  let exp2 = "";

  for(let k = 1; k <= N; ++k) {
    exp1 += `${k}^2 + `;
    exp2 += `${k*k} + `;
  }

  console.log(exp1);
  console.log(exp2);
}

//-----------------------------------------------------------------------------
// Σ{k=1}{n-1}(2k)
//-----------------------------------------------------------------------------
function work02() {

  let exp1 = "";
  let exp2 = "";

  for(let k = 1; k <= N-1; ++k) {
    exp1 += `2*${k} + `;
    exp2 += `${2*k} + `;
  }

  console.log(exp1);
  console.log(exp2);
}

work02();